'''
Escreva um programa que leia um valor em metros e o exiba convertido em centimetros
e milimetros.
'''

metros = float(input("Digite a distancia em metros: "))
centimetros = metros * 100
milimetros = metros * 1000

print(f"A variável metros foi convertida em centimetros, o resultado é: {centimetros} centrimetros")
print(f"A variável metros foi convertida em milimetros, o resultado é: {milimetros} milimetros")
