'''
Faça um programa que receba dois números e some
'''
# Variáveis que vão receber os números do usúario
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

# Variável responsavel pela soma
soma = n1 + n2

# Formatação para que retorne ao usúario o resultado da soma
print(f'A soma entre {n1} e {n2} é igual a {soma}')