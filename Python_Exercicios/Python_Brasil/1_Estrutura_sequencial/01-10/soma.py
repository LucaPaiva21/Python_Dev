'''
Faça um Programa que peça dois números e imprima a soma
'''
# Variáveis que receberam o número para soma, pelo proprio usúario
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

# Variável responsavel pela soma dos dois números
soma = numero1 + numero2

# Mostra o resultado na tela para o usúario
print(f"A soma entre {numero1} e {numero2} é igual a {soma}")