'''
Faça um Programa que converta metros para centímetros.
'''
# Recebe o valor pelo usúario, para ser convertido
metros = float(input("Digite a quantidade de metros: "))

# Variável responsavel pela conversão
centimetros = metros * 100

# Mostra o resultado para o usúario
print(f"A conversão para centímetro foi feita, o resultado é: {centimetros}cm")